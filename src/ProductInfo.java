    public ArrayList<User> userList() {
        ArrayList<User> userList = new ArrayList<>();

        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/smobile","root","paimon");
            String query1 = "select * from products";
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery(query1);
            User user;
            while(rs.next()) {
                user = new User( rs.getInt("id"), rs.getInt("price"), rs.getInt("qty"), rs.getString("pname"));
                userList.add(user);
            }
            
            
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(ProductInfo.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(ProductInfo.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        return userList;
        
    }
    
    public void show_user() {
        ArrayList<User> list = userList();
        DefaultTableModel model = (DefaultTableModel) jTable1_Display_User.getModel();
        Object[] row = new Object[4];
        for(int i=0; i<list.size(); i++) {
            row[0] = list.get(i).getId();
            row[1] = list.get(i).getPname();
            row[2] = list.get(i).getPrice();
            row[3] = list.get(i).getQty();
            model.addRow(row);
        }
    }
    
    

